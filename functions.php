/**
 * Register Google font for plazarecoleta.
 *
 * @since plazarecoleta 1.0
 *
 * @return string
 */
function plazarecoleta_font_url() {
	$font_url = '';
	/*
	 * Translators: If there are characters in your language that are not supported
	 * by Lato, translate this to 'off'. Do not translate into your own language.
	 */
	if ( 'off' !== _x( 'on', 'Font: on or off', 'plazarecoleta' ) ) {
		$font_url = add_query_arg( 'family','Roboto:400,300,400italic,700|Roboto+Slab', "//fonts.googleapis.com/css" );

	}

	return $font_url;
}

