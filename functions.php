/**
 * Find out if blog has more than one category.
 *
 * @since plazarecoleta 1.0
 *
 * @return boolean true if blog has more than 1 category
 */
function plazarecoleta_categorized_blog() {
	if ( false === ( $all_the_cool_cats = get_transient( 'simple_category_count' ) ) ) {
		// Create an array of all the categories that are attached to posts
		$all_the_cool_cats = get_categories( array(
			'hide_empty' => 1,
		) );

		// Count the number of categories that are attached to the posts
		$all_the_cool_cats = count( $all_the_cool_cats );

		set_transient( 'simple_category_count', $all_the_cool_cats );
	}

	if ( 1 !== (int) $all_the_cool_cats ) {
		// This blog has more than 1 category so simple_categorized_blog should return true
		return true;
	} else {
		// This blog has only 1 category so simple_categorized_blog should return false
		return false;
	}
}


